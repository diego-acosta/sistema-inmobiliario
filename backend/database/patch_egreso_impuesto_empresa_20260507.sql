    ALTER COLUMN uid_global SET NOT NULL,
    ALTER COLUMN uid_global SET DEFAULT gen_random_uuid(),
    ALTER COLUMN version_registro SET NOT NULL,
    ALTER COLUMN version_registro SET DEFAULT 1,
    ALTER COLUMN created_at SET NOT NULL,
    ALTER COLUMN created_at SET DEFAULT CURRENT_TIMESTAMP,
    ALTER COLUMN updated_at SET NOT NULL,
    ALTER COLUMN updated_at SET DEFAULT CURRENT_TIMESTAMP,
    ALTER COLUMN id_comprobante_impuesto SET NOT NULL,
    ALTER COLUMN id_movimiento_tesoreria SET NOT NULL,
    ALTER COLUMN fecha_pago SET NOT NULL,
    ALTER COLUMN importe_pagado SET NOT NULL,
    ALTER COLUMN estado_egreso SET NOT NULL,
    ALTER COLUMN estado_egreso SET DEFAULT 'REGISTRADO';

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint
        WHERE conname = 'chk_eie_importe'
          AND conrelid = 'public.egreso_impuesto_empresa'::regclass
    ) THEN
        ALTER TABLE public.egreso_impuesto_empresa
            ADD CONSTRAINT chk_eie_importe CHECK (importe_pagado > 0);
    END IF;

    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint
        WHERE conname = 'chk_eie_estado'
          AND conrelid = 'public.egreso_impuesto_empresa'::regclass
    ) THEN
        ALTER TABLE public.egreso_impuesto_empresa
            ADD CONSTRAINT chk_eie_estado
            CHECK (estado_egreso IN ('REGISTRADO', 'ANULADO'));
    END IF;

    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint
        WHERE conname = 'fk_eie_comprobante_impuesto'
          AND conrelid = 'public.egreso_impuesto_empresa'::regclass
    ) THEN
        ALTER TABLE public.egreso_impuesto_empresa
            ADD CONSTRAINT fk_eie_comprobante_impuesto
            FOREIGN KEY (id_comprobante_impuesto)
            REFERENCES public.comprobante_impuesto(id_comprobante_impuesto)
            ON DELETE RESTRICT;
    END IF;

    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint
        WHERE conname = 'fk_eie_movimiento_tesoreria'
          AND conrelid = 'public.egreso_impuesto_empresa'::regclass
    ) THEN
        ALTER TABLE public.egreso_impuesto_empresa
            ADD CONSTRAINT fk_eie_movimiento_tesoreria
            FOREIGN KEY (id_movimiento_tesoreria)
            REFERENCES public.movimiento_tesoreria(id_movimiento_tesoreria)
            ON DELETE RESTRICT;
    END IF;
END $$;

CREATE INDEX IF NOT EXISTS idx_eie_comprobante_impuesto
    ON public.egreso_impuesto_empresa USING btree (id_comprobante_impuesto);

CREATE INDEX IF NOT EXISTS idx_eie_movimiento_tesoreria
    ON public.egreso_impuesto_empresa USING btree (id_movimiento_tesoreria);

CREATE INDEX IF NOT EXISTS idx_eie_uid_global
    ON public.egreso_impuesto_empresa USING btree (uid_global);

CREATE UNIQUE INDEX IF NOT EXISTS ux_eie_op_id_alta_activo
    ON public.egreso_impuesto_empresa USING btree (op_id_alta)
    WHERE ((op_id_alta IS NOT NULL) AND (deleted_at IS NULL));

