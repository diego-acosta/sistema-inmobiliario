-- Registra pagos/egresos de la empresa por comprobante_impuesto mediante tesoreria.

CREATE TABLE IF NOT EXISTS public.egreso_impuesto_empresa (
    id_egreso_impuesto_empresa bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uid_global uuid NOT NULL DEFAULT gen_random_uuid(),
    version_registro integer NOT NULL DEFAULT 1,
    created_at timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at timestamp without time zone,
    id_instalacion_origen bigint,
    id_instalacion_ultima_modificacion bigint,
    op_id_alta uuid,
    op_id_ultima_modificacion uuid,
    id_comprobante_impuesto bigint NOT NULL,
    id_movimiento_tesoreria bigint NOT NULL,
    fecha_pago date NOT NULL,
    importe_pagado numeric(14,2) NOT NULL,
    medio_pago character varying(50),
    referencia_comprobante character varying(100),
    estado_egreso character varying(30) NOT NULL DEFAULT 'REGISTRADO',
    observaciones text
);

