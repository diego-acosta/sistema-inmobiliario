    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_lite_liquidacion') THEN
        ALTER TABLE public.liquidacion_impuesto_trasladado_egreso ADD CONSTRAINT fk_lite_liquidacion FOREIGN KEY (id_liquidacion_impuesto_trasladado) REFERENCES public.liquidacion_impuesto_trasladado(id_liquidacion_impuesto_trasladado) ON DELETE RESTRICT;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_lite_egreso') THEN
        ALTER TABLE public.liquidacion_impuesto_trasladado_egreso ADD CONSTRAINT fk_lite_egreso FOREIGN KEY (id_egreso_impuesto_empresa) REFERENCES public.egreso_impuesto_empresa(id_egreso_impuesto_empresa) ON DELETE RESTRICT;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_litr_liquidacion') THEN
        ALTER TABLE public.liquidacion_impuesto_trasladado_responsable ADD CONSTRAINT fk_litr_liquidacion FOREIGN KEY (id_liquidacion_impuesto_trasladado) REFERENCES public.liquidacion_impuesto_trasladado(id_liquidacion_impuesto_trasladado) ON DELETE RESTRICT;
