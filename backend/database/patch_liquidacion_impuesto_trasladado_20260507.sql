CREATE UNIQUE INDEX IF NOT EXISTS ux_lite_egreso_activo
    ON public.liquidacion_impuesto_trasladado_egreso USING btree (id_egreso_impuesto_empresa)
    WHERE ((deleted_at IS NULL) AND ((estado_liquidacion_impuesto_egreso)::text = 'ACTIVO'::text));
CREATE UNIQUE INDEX IF NOT EXISTS ux_lit_codigo_activo
    ON public.liquidacion_impuesto_trasladado USING btree (codigo_liquidacion_impuesto_trasladado)
    WHERE ((codigo_liquidacion_impuesto_trasladado IS NOT NULL) AND (deleted_at IS NULL));
CREATE UNIQUE INDEX IF NOT EXISTS ux_lit_op_id_alta_activo
    ON public.liquidacion_impuesto_trasladado USING btree (op_id_alta)
    WHERE ((op_id_alta IS NOT NULL) AND (deleted_at IS NULL));

DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_lit_relacion_generadora') THEN
        ALTER TABLE public.liquidacion_impuesto_trasladado ADD CONSTRAINT fk_lit_relacion_generadora FOREIGN KEY (id_relacion_generadora) REFERENCES public.relacion_generadora(id_relacion_generadora) ON DELETE RESTRICT;
