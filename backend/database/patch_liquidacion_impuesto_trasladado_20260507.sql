    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_litc_liquidacion') THEN
        ALTER TABLE public.liquidacion_impuesto_trasladado_comprobante ADD CONSTRAINT fk_litc_liquidacion FOREIGN KEY (id_liquidacion_impuesto_trasladado) REFERENCES public.liquidacion_impuesto_trasladado(id_liquidacion_impuesto_trasladado) ON DELETE RESTRICT;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_litc_comprobante') THEN
